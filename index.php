              <h2 style="font-family: Times New Roman;"><center><b>THE UNIVERSITY ACRONYM: IFSU</b></center></h2>
              <p style="text-indent: 3em; font-family: Times New Roman; font-size: 120%; text-align: justify;">
                The official acronym of the Ifugao State University shall be IFSU to refer to the following: “IF” for Ifugao to put emphasis on the host province whose people are the immediate clientele of the university, “S” for State to mean that the university is supported by the Philippine Government and “U” for University. The acronym would avoid potential intellectual property rights conflict with the Isabela State University which uses ISU as its acronym. Henceforth, the Ifugao State University claims exclusive rights on the acronym IFSU.
              </p>

              <h3 style="font-family: Times New Roman;"><center><b>The IFSU LOGO</b></center></h3>
              <p style="text-indent: 3em; font-family: Times New Roman; font-size: 120%; text-align: justify;">
                The official logo symbolizes the humble beginnings and noble aspirations of the Ifugao State University. It shall be what is shown below.
              </p>

              <p style="text-indent: 3em; font-family: Times New Roman; font-size: 120%; text-align: justify;">
                The logo is shaped like the “PANG-O”, the gold amulet that adorn the royal people or the “kadangyan” of Ifugao which is a symbol of prestige and authority. In the traditional Ifugao society, the “kadangyans” are the leaders of the village who are generally generous and well respected by the community people.
              </p>

              <p style="text-indent: 3em; font-family: Times New Roman; font-size: 120%; text-align: justify;">
                The torch on which eleven bright flames radiate from a book upon its threshold portrays IFSU as the source of knowledge and truth as an educational institution. The flames represent the acronym IFSU and the light rays represent the 11 municipalities of the Ifugao the immediate clientele of the university.
              </p>

              <p style="text-indent: 3em; font-family: Times New Roman; font-size: 120%; text-align: justify;">
                Embedded at the center is the head of a carabao, symbolizing strength and perseverance. The right side of the horn represents the humble beginning of IFSU being a primary agriculture and forestry institution further symbolized by the green mountains and majestic rice terraces. The left side of the horn with gear-shape with computer at the back represents continuous advancements and expansion towards industrial, technological and scientific fields. The six (6) Ifugao native houses represent the campuses: Main Campus, Potia Campus, Lagawe Campus, Tinoc Campus, Aguinaldo Campus and Hapao Campus. The four (4) terraces represent the fourfold thrust of the University namely: Instruction, Research, extension and Production/ Resource Generation.
              </p>

              <p style="text-indent: 3em; font-family: Times New Roman; font-size: 120%; text-align: justify;">
                Dominantly colored green and golden yellow, the logo reflects the official colors of the University.
              </p>
